/** frontpage animation **/

var x = document.querySelectorAll('#one');
var i = 0;

while(i < x.length){
    x[i].onmouseover = function change(){
        var i = 0;
        while(i < x.length){
            x[i].style.color = '#B2584D';
            i++;
        }
    }

    x[i].onmouseout = function change(){
        var i = 0;
        while(i < x.length){
            x[i].style.color = '';
            i++;
        }
    }

    i++;
}

